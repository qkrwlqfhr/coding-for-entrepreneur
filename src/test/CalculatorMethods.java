package test;

public class CalculatorMethods {
    public Double sum(double a, double b){
        return a + b;
    }

    public Double substract(double a, double b){
        return a - b;
    }

    public Double multifly(double a, double b){
        return a * b;
    }

    public Double divide(double a, double b){
        return a / b;
    }
}
